    <h2>Syarat dan Ketentuan Penyewaan di Orbit Outdoor</h2>
    <ul>
        <li>Saat pengambilan perlengkapan/barang, setiap penyewa wajib meninggalkan kartu identitas berupa
            E-KTP/SIM/Kartu Pelajar / KTM (minimal angkatan di atas 2020), yang masih berlaku dan berdomisili di
            Jabodetabek.</li>
        <li>Untuk E-KTP di luar Jabodetabek bisa dikenakan jaminan uang senilai harga jual barang.</li>
        <li>Penjadwalan perlengkapan persewaan hanya diperuntukan bagi penyewa/pelanggan yang telah membayar Down
            Payment (DP).</li>
        <li>Besarnya Down Payment (DP) adalah minimal 25% dari seluruh biaya sewa.</li>
        <li>Untuk pemesanan partai besar (seluruh biaya sewa di atas 2 juta) minimal DP 50%.</li>
        <li>Harga sewa adalah per 1 (satu) hari atau 24 jam dan minimal sewa 2 (dua) hari.</li>
        <li>Jika terjadi keterlambatan pengembalian maka dianggap memperpanjang sewa (toleransi keterlambatan
            pengembalian 1-2 jam).</li>
        <li>Bawalah nota anda saat pengambilan maupun pengembalian perlengkapan/barang.</li>
        <li>Perlengkapan/barang yang sudah dibawa keluar dari area persewaan Orbit Outdoor sudah dianggap sewa.</li>
        <li>Ketentuan tersebut di atas dapat berubah sesuai dengan situasi dan kondisi.</li>
        <li>Perlengkapan/barang yang sudah dibawa keluar dari area persewaan Orbit Outdoor sudah dianggap sewa.</li>
        <li>Point-point tersebut di atas merupakan kesepakatan yang harus ditaati oleh penyewa.</li>
        <li>Selamat berlibur, tetap berhati-hati dan waspada. Adventure has no limit.</li>
    </ul>
